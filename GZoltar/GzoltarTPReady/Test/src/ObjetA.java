
public class ObjetA {
	private int a;
	private int b;
	ObjetA(int a, int b){
		this.a = a;
		this.b = b;
	}
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	public int aFois2(){
		return a*2;
	}
}
